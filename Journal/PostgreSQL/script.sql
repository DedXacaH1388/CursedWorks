
-- Dropping tables before creating new
DROP TABLE IF EXISTS teachers	CASCADE;
DROP TABLE IF EXISTS classes	CASCADE;
DROP TABLE IF EXISTS students	CASCADE;
DROP TABLE IF EXISTS subjects	CASCADE;
DROP TABLE IF EXISTS lessons	CASCADE;
DROP TABLE IF EXISTS journal	CASCADE;


-- teachers table
CREATE TABLE teachers (
	teacher_id		integer				GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	teacher_name	varchar(128)		NOT NULL,
	pass_hash		varchar(64)		NOT NULL
);

ALTER TABLE teachers
	ADD CONSTRAINT pk_teachers PRIMARY KEY (teacher_id);


-- classes table
CREATE TABLE classes (
	class_id		integer				GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	class_name		varchar(128)		NOT NULL
);

ALTER TABLE classes 
	ADD CONSTRAINT pk_classes PRIMARY KEY (class_id);


-- students table
CREATE TABLE students (
	student_id		integer				GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	class_id		integer				NOT NULL,
	student_name	varchar(128)		NOT NULL,
	skips			integer				DEFAULT(0) NOT NULL
);

ALTER TABLE students
	ADD CONSTRAINT pk_students PRIMARY KEY (student_id),
	ADD CONSTRAINT fk_students_classes FOREIGN KEY (class_id) REFERENCES classes;


-- subjects_table
CREATE TABLE subjects (
	subject_id		integer				GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	subject_name	varchar(128)		NOT NULL,
	teacher_id		integer				NOT NULL,
	class_id		integer				NOT NULL,
	hours			decimal				DEFAULT(0) NOT NULL
);

ALTER TABLE subjects
	ADD CONSTRAINT pk_subjects PRIMARY KEY (subject_id),
	ADD CONSTRAINT fk_subjects_teachers FOREIGN KEY (teacher_id) REFERENCES teachers,
	ADD CONSTRAINT fk_subjects_classes FOREIGN KEY (class_id) REFERENCES classes;
	

-- lessons table
CREATE TABLE lessons (
	lesson_id		integer				GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	subject_id		integer				NOT NULL,
	lesson_date		date				DEFAULT(CURRENT_DATE) NOT NULL,
	theme			varchar(1024)
);

ALTER TABLE lessons
	ADD CONSTRAINT pk_lessons PRIMARY KEY (lesson_id),
	ADD CONSTRAINT fk_lessons_subjects FOREIGN KEY (subject_id) REFERENCES subjects;
	

-- journal table
CREATE TABLE journal (
	record_id		integer				GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	lesson_id		integer				NOT NULL,
	student_id		integer				NOT NULL,
	mark			integer				DEFAULT (0) NOT NULL
	-- mark == 0 -> skip; mark > 0 && mark <= 10 -> default mark; mark > 10 && mark <= 20 -> exam mark; mark == 21 -> laboratory work
);

ALTER TABLE journal
	ADD CONSTRAINT pk_journal PRIMARY KEY (record_id),
	ADD CONSTRAINT fk_journal_lessons FOREIGN KEY (lesson_id) REFERENCES lessons,
	ADD CONSTRAINT fk_journal_students FOREIGN KEY (student_id) REFERENCES students;



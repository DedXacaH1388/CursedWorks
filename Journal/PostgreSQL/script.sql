DROP FUNCTION IF EXISTS get_classes, get_subjects;


-- get class info for left menu
CREATE FUNCTION get_classes (_teacher_id integer)
RETURNS TABLE (
	class_id	integer,
	class_name	VARCHAR
)
AS $$
BEGIN
	RETURN QUERY
	SELECT classes.class_id, classes.class_name 
	FROM classes
	WHERE classes.class_id IN (	SELECT subjects.class_id
					   			FROM subjects
					   			WHERE subjects.teacher_id = _teacher_id
							  );
END;
$$ LANGUAGE 'plpgsql';


-- get subject info for left menu
CREATE FUNCTION get_subjects (_class_id integer)
RETURNS TABLE (
	subject_id		integer,
	subject_name	VARCHAR
)
AS $$
BEGIN
	RETURN QUERY
	SELECT subjects.subject_id, subjects.subject_name
	FROM subjects 
	WHERE subjects.class_id = _class_id;
END;
$$ LANGUAGE 'plpgsql';

DROP FUNCTION IF EXISTS get_teachers, pass_valid;


-- get teacher names for logging in
CREATE FUNCTION get_teachers ()
RETURNS TABLE (
	teacher_id		integer,
	teacher_name	VARCHAR
)
AS $$
BEGIN
	RETURN QUERY 
	SELECT teachers.teacher_id, teachers.teacher_name
	FROM teachers
	ORDER BY teacher_name;
END;
$$ LANGUAGE 'plpgsql';


-- check if password hash valid
CREATE FUNCTION pass_valid(_teacher_id integer, hash varchar(64))
RETURNS integer
AS $$
DECLARE
	_hash varchar(64);
BEGIN
	_hash = upper((SELECT teachers.pass_hash FROM teachers WHERE teacher_id = _teacher_id));
	if _hash = hash then
		RETURN 1;
	else 
		RETURN 0;
	end if;
END;
$$ LANGUAGE 'plpgsql';

DROP FUNCTION IF EXISTS get_dates, get_students, get_marks;


-- get dates for grid headers
CREATE FUNCTION get_dates(_subject_id integer)
RETURNS TABLE (
	lesson_id	integer,
	lesson_date date
)
AS $$
BEGIN
	RETURN QUERY
	SELECT lessons.lesson_id, lessons.lesson_date
	FROM lessons
	WHERE lessons.subject_id = _subject_id
	ORDER BY lessons.lesson_date;
END;
$$ LANGUAGE 'plpgsql';

-- get sudent names for grid headers
CREATE FUNCTION get_students (_subject_id integer)
RETURNS TABLE (
	student_id		integer,
	student_name	VARCHAR
)
AS $$
BEGIN
	RETURN QUERY
	SELECT students.student_id, students.student_name
	FROM students
	WHERE students.class_id = (SELECT subjects.class_id
							   FROM subjects
							   WHERE subject_id = _subject_id)
	ORDER BY students.student_name;
END;
$$ LANGUAGE 'plpgsql';

--	get marks for every student
CREATE FUNCTION get_marks (_student_id integer, _subject_id integer)
RETURNS TABLE (
	lesson_id	integer,
	mark		integer
)
AS $$
BEGIN
	RETURN QUERY
	SELECT journal.lesson_id, journal.mark
	FROM journal
	WHERE	journal.student_id = _student_id AND
			journal.lesson_id IN (	SELECT lessons.lesson_id
						  			FROM lessons
						  			WHERE lessons.subject_id = _subject_id);
END;
$$ LANGUAGE 'plpgsql';


